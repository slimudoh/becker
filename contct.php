    <script type="text/javascript">
      
      function openMenu() {
        //document.getElementById("dropmenu").style.width = "100%";       

        document.getElementById("dropmenu").style.WebkitAnimation = "moveout .5s";
        document.getElementById("dropmenu").style.animation = "moveout .5s";
        document.getElementById("dropmenu").style.width = "100%";        
      }

      function closeMenu() {
        document.getElementById("dropmenu").style.WebkitAnimation = "movein .5s";
        document.getElementById("dropmenu").style.animation = "movein .5s";
        document.getElementById("dropmenu").style.width = "0%";
      }

    </script>

    <script type="text/javascript">
      onresize = onload = function() {
        var desktopMenu = document.getElementById("desktop");
        var mobileMenu = document.getElementById("mobile");
        var cover = document.getElementById("cover");
        var dropMenu = document.getElementById("dropmenu");


        if (window.innerWidth < 1000 || document.documentElement.clientWidth < 1000 || document.body.clientWidth < 1000)  {
          desktopMenu.style.display = "none";
          mobileMenu.style.display = "block";
        } else if (window.innerWidth >= 1000 || document.documentElement.clientWidth >= 1000 || document.body.clientWidth >= 1000) {
          desktopMenu.style.display = "block";
          mobileMenu.style.display = "none";
        }

        if (window.innerWidth < 1000 || document.documentElement.clientWidth < 1000 || document.body.clientWidth < 1000)  {
          dropMenu.style.display = "initial";
        } else if (window.innerWidth >= 1000 || document.documentElement.clientWidth >= 1000 || document.body.clientWidth >= 1000) {
          dropMenu.style.display = "none";
        }


        if (window.innerWidth < 500 || document.documentElement.clientWidth < 500 || document.body.clientWidth < 500) {
          cover.style.fontSize = "30px";
        } else {
          cover.style.fontSize = "50px";
        } 
      }   

    </script>




